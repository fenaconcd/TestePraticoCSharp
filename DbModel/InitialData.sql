        FROM Produto
        
        OUTER APPLY (
            SELECT SUM(PercComsSW) PercComsSW, SUM(PercComsHW) PercComsHW
                FROM ComsCargo
                WHERE ProdutoId = Produto.Id
                GROUP BY ProdutoId
