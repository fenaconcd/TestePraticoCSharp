INSERT INTO ComsCargo (ProdutoId, Cargo, PercComsSW, PercComsHW)
    SELECT
        Id,
        @Cargo Cargo,
        IIF(Produto.ValorSW > 0 AND (Produto.PercFornecedorSW + COALESCE(ComsAcum.PercComsSW, 0)) < @ComsTotal, @ComsTotal - Produto.PercFornecedorSW - COALESCE(ComsAcum.PercComsSW, 0), 0),
